    private void viewAdmin(Admin b) {
        this.tfadid.setText(b.getAdID());
        this.tfadname.setText(b.getAdName());
        this.tfaduser.setText(b.getAdUser());
        this.tfadpass.setText(b.getAdPass());
        this.tfademail.setText(b.getEmail());
        if (b.getSex().equalsIgnoreCase("Male")) {
            jComboBoxSex.setSelectedItem("Male");
        } else if (b.getSex().equalsIgnoreCase("Female")) {
            jComboBoxSex.setSelectedItem("Female");
        }
//        Admin, Manager, Supplier Manager, Statistics Staff
        if(b.getRole().equalsIgnoreCase("Admin")){
            jComboBoxRole.setSelectedItem("Admin");
        } else if(b.getRole().equalsIgnoreCase("Manager")){
            jComboBoxRole.setSelectedItem("Manager");
        } else if(b.getRole().equalsIgnoreCase("Supplier Manager")){
            jComboBoxRole.setSelectedItem("Supplier Manager");
        } else if(b.getRole().equalsIgnoreCase("Statistics Staff")){
            jComboBoxRole.setSelectedItem("Statistics Staff");
        }       
    }
