    private void btChangePassActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btChangePassActionPerformed
        if (tfUserName.getText().equalsIgnoreCase("") || tfNewPass.getText().equalsIgnoreCase("") || tfCurrentPass.getText().equalsIgnoreCase("") || tfComfirm.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "Please fill all text boxs");
        } else {
            try {
                // TODO add your handling code here:
                ResultSet rs = data.getData("Select * from admin "
                        + "where UserName='" + tfUserName.getText() + "'");

                if (rs.next()) {
                    String userNameDB = rs.getString("UserName");
                    String passwordDB = rs.getString("Pass");
                    if (userNameDB.equals(tfUserName.getText()) && passwordDB.equals(new String(tfCurrentPass.getPassword()))) {
                        if(tfNewPass.getText().equals(tfComfirm.getText())){
                          data.updateDB("update admin set Pass = '" + tfComfirm.getText() + "' where UserName = '" + tfUserName.getText() +"'");
                          JOptionPane.showMessageDialog(this, "Change Password Successfully");
                        } else {
                            JOptionPane.showMessageDialog(this, "Confirm Failed");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid Account");
                    }
                }

            } catch (SQLException ex) {
                System.out.println("Error: " + ex.getMessage());;
            }
        }
    }//GEN-LAST:event_btChangePassActionPerformed

